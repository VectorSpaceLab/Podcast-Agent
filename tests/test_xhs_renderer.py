def test_split_paragraph_block_uses_sentence_prefix_when_possible() -> None:
    block = _measured("b1", "paragraph", 900, text="第一句可以放进去。第二句留在下一页。第三句继续。")

    split = split_paragraph_block(block, available_height=360, min_prefix_chars=4)

    assert split is not None
    head, tail = split
    assert head.text == "第一句可以放进去。"
    assert tail.text == "第二句留在下一页。第三句继续。"
    assert tail.continuation is True


def _measured(
    block_id: str,
    kind: str,
    height: float,
    warning: str | None = None,
    text: str | None = None,
    split_from: str | None = None,
    split_part: int | None = None,
    continuation: bool = False,
) -> XhsMeasuredBlock:
    value = text or block_id
    return XhsMeasuredBlock(
        id=block_id,
        kind=kind,
        text=value,
        block={"kind": kind, "text": value, "continuation": continuation},
        outer_height=height,
        warning=warning,
        split_from=split_from,
        split_part=split_part,
        continuation=continuation,
    )


def _page_height_for_test(page: list[XhsMeasuredBlock]) -> float:
    return sum(block.outer_height for block in page)
